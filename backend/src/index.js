// Middleware de Mantenimiento Global
// ==========================
app.use(async (req, res, next) => {
    const estado = await global.getMaintenanceStatus();

    // Verificar si el límite de aviso previo por mantenimiento ya expiró
    if (estado.advertencia && estado.limite) {
        if (new Date() >= new Date(estado.limite)) {
            estado.bloqueo = true;
            estado.advertencia = false;
            estado.limite = '';
            
            // Sincronizar con la base de datos de manera asíncrona
            poolPromise.then(pool => {
                return pool.request().query(`
                    UPDATE Configuracion SET valor = 'true' WHERE clave = 'bloqueo_mantenimiento';
                    UPDATE Configuracion SET valor = 'false' WHERE clave = 'advertencia_mantenimiento';
                    UPDATE Configuracion SET valor = '' WHERE clave = 'mantenimiento_limite';
                `);
            }).then(() => {
                logger.info("⚡ [Mantenimiento] El tiempo límite expiró. El bloqueo general ha sido activado automáticamente.");
                global.invalidateMaintenanceCache();
            }).catch(dbErr => {
                logger.error("❌ Error al guardar bloqueo automático en base de datos: " + dbErr.message);
            });
        }
    }

    if (estado.bloqueo || process.env.MAINTENANCE_MODE === 'true') {
        if (req.path.startsWith('/api/auth/')) {
            return next();
        }

        const authHeader = req.headers.authorization;
        if (authHeader && authHeader.startsWith('Bearer ')) {
            const token = authHeader.split(' ')[1];
            try {
                const decoded = jwt.verify(token, process.env.JWT_SECRET);
                if (decoded.rol === 'admin') {
                    return next();
                }
            } catch (err) {
                // Token inválido o expirado
            }
        }

        return res.status(503).json({
            status: 'maintenance',
            message: 'El sistema se encuentra temporalmente fuera de servicio por tareas de mantenimiento y actualización.'
        });
    }
    next();
});

