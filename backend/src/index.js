let maintenanceCache = {
    advertencia: false,
    bloqueo: false,
    limite: '',
    lastFetched: 0
};

global.getMaintenanceStatus = async function() {
    const now = Date.now();
    if (now - maintenanceCache.lastFetched < 30000) {
        return maintenanceCache;
    }

    try {
        const pool = await poolPromise;
        const res = await pool.request().query("SELECT clave, valor FROM Configuracion");
        
        let advertencia = false;
        let bloqueo = false;
        let limite = '';
        
        res.recordset.forEach(row => {
            if (row.clave === 'advertencia_mantenimiento') {
                advertencia = row.valor === 'true';
            }
            if (row.clave === 'bloqueo_mantenimiento') {
                bloqueo = row.valor === 'true';
            }
            if (row.clave === 'mantenimiento_limite') {
                limite = row.valor;
            }
        });

        maintenanceCache = {
            advertencia,
            bloqueo,
            limite,
            lastFetched: now
        };
    } catch (err) {
        console.error("❌ Error al refrescar caché de mantenimiento:", err.message);
    }
    return maintenanceCache;
};

global.invalidateMaintenanceCache = function() {
    maintenanceCache.lastFetched = 0;
};

